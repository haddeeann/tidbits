            middle = 1
            right = 2
            # first value
            sum = nums[left] + nums[middle] + nums[right]
            diff = abs(target - sum)
            if target < sum:
                return sum
            # test value
            sum_test = nums[left] + nums[middle] + nums[right+1]
            test_diff = abs(target-sum_test)
            # if we get further away
            if diff < test_diff:
                return sum

            while diff > test_diff:
                diff = test_diff
                right += 1
                sum_test = nums[left] + nums[middle] + nums[right]
                test_diff = abs(target - sum_test)

            while diff > test_diff:
                middle += 1
                sum_test = nums[left] + nums[middle] + nums[right]
                test_diff = abs(target - sum_test)


